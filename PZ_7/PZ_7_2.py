# Вводим строки S, S1 и S2
S = input("Введите строку S: ")
S1 = input("Введите строку S1 для замены: ")
S2 = input("Введите строку S2, на которую нужно заменить: ")

# Находим индекс последнего вхождения S1 в S
index = S.rfind(S1)

# Проверяем, найдено ли вхождение
if index != -1:
    # Заменяем последнее вхождение S1 на S2
    result = S[:index] + S2 + S[index + len(S1):]
else:
    # Если S1 не найдено, оставляем строку без изменений
    result = S

# Выводим результат
print("Результат:", result)
